    <p> <h3>Kako mogu izbrisati kolačiće, kontrolirati ih i onemogućiti?</h3>
<br>
<br>
  Svaki posjetitelj web stranice može lako kontrolirati kolačiće koje je primio preglednik, izbrisati ih ili onemogućiti jednostavno promjenom postavki korištenog preglednika. 
  Međutim, važno je imati na umu da onemogućavanje navigacije ili funkcionalnih kolačića može utjecati na rad web stranice i kvalitetu pruženih usluga. 
  Brisanje nakupljenih kolačića može rezultirati gubitkom podataka o navikama pregledavanja posjetitelja, tako da web mjesto više neće biti personalizirano. 
